	ADD FOREIGN KEY (FK_DELIVERY_ORDER)
	REFERENCES BTS.DELIVERY_ORDER (ID)
	ON UPDATE NO ACTION
	ON DELETE NO ACTION
;

ALTER TABLE BTS.DELIVERY_ORDER_DETAIL
	ADD FOREIGN KEY (FK_PRODUCT)
	REFERENCES BTS.PRODUCT (ID)
	ON UPDATE NO ACTION
	ON DELETE NO ACTION
;

ALTER TABLE BTS.DELIVERY_ORDER_DETAIL
